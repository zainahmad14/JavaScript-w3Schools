const num1 = 5;
let result = 10;

// Assignment Assignment Operator

result = num1;
console.log(result);

// Increment Assignment Operator

result += num1;
console.log(result);

// Decrement Assignment Operator

result -= num1;
console.log(result);

// Multiplication Assignment Operator

result *= num1;
console.log(result);

// Division Assignment Operator

result /= num1;
console.log(result);

// Modulus Assignment Operator

result %= num1;
console.log(result);

// Exponentionation Assignment Operator

result **= num1;
console.log(result);

// Not Equal Assignment Operator

result != num1;
console.log(result);
