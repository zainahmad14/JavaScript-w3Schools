let str = 'My name is \"Zain Ahmad"';

// indexOf()
// returns index of substring to be found

console.log(str.indexOf('Ahmad'));

// lastIndexOf()
// returns last index position of subtring to 
// be found

console.log(str.lastIndexOf('Ahmad'));

// startsWith()
// returns index of starting position of
// substring to be located

console.log(str.startsWith('M'));

// endsWith()
// returns index of ending position of 
// substring to be located

console.log(str.endsWith('\"'));
